/* Arrays is a linear data structure which basically stores values in a contiguous manner. it's zero index base. and it's not resizable once created.*/

//Store the fist 10 natural numbers and them(traverse)
public class Array{
     public static void main(String [] args){
     int []arr = new int[10];//initialization and declaration 
     for(int i = 0; i<arr.length;i++){//traversing
         System.out.println(arr[i]);
     }
    }
}
        
